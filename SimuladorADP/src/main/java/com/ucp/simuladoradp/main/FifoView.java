    public FifoView() {
        initComponents();
        ArrayList<Proceso> listado = new ArrayList();
       int orden = 10;
        for (int i = 0; i < 10; i++) {
            Proceso p = new Proceso();
            p.setInstante_entrada(orden);
            p.setNombre("p"+i);
            p.setDuracion(2);
            orden -= 1;
            p.setId(i);
            listado.add(p);
        }
        listProcess = listado;
        procesosTableModel = new ProcesoTableModel(listado);
        jTableProcesos.setModel(procesosTableModel);
    }

